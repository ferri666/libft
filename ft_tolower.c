static int	is_special(int c)
{
	if (c == -127 || c == -119 || c == -115
		|| c == -109 || c == -102 || c == -121
		|| c == -111 || c == -128 || c == -120
		|| c == -116 || c == -110 || c == -103
		|| c == -126 || c == -118 || c == -114
		|| c == -108 || c == -101 || c == -124
		|| c == -117 || c == -113 || c == -106
		|| c == -100)
		return (1);
	return (0);
}

int	ft_tolower(int c)
{
	if ((c >= 65 && c <= 90) || is_special(c))
		c += 32;
	return (c);
}
